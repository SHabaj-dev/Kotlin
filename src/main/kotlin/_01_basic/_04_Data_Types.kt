package _01_basic


fun main() {
    //Integer Data Types
    val integerNumber: Int = 10
    val byteNumber: Byte = 12
    val shorNumber: Short = 2
    val longNumber: Long = 12345678L

    //Floating Points
    val float: Float = 2.34f
    val double: Double = 2.564567

    //Character Data Types
    val character: Char = 'a'

    //String Data Type
    val stringExample: String = "Something is Stringing"

    //Boolean DataTypes
    val isOnline: Boolean = false

    println("Integer Numbers Examples are \n $integerNumber, $byteNumber, $shorNumber, $longNumber")
    println("Floating Point Numbers Examples are \n $float, $double")
    println("Character Example is $character")
    println("String Example is $stringExample")
    println("Boolean Example is $isOnline")


}